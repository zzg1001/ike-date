

def find_keywords(text, keywords):
    # 将文本转换为小写，以便匹配不区分大小写
    text_lower = text.lower()
    # 找出文本中所有的关键词
    found_keywords = [keyword for keyword in keywords if keyword in text_lower]
    return found_keywords

